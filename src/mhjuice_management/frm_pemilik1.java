public void refreshTable() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Petugas");
    model.addColumn("Nama Petugas");
    model.addColumn("Password");

    tabel_petugas.setModel(model);

    try {
        String sql = "SELECT id_pengguna, nama_pengguna, kata_sandi FROM pengguna WHERE tugas = 1";
        PreparedStatement stat = k.getCon().prepareStatement(sql);
        ResultSet rs = stat.executeQuery();

        while (rs.next()) {
            Object[] data = {
                rs.getInt("id_pengguna"),
                rs.getString("nama_pengguna"),
                rs.getString("kata_sandi")
            };
            model.addRow(data);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "SQL Error: " + e.getMessage());
    }
}

     
          private void setPlaceholder(JTextField field, String placeholder) {
    field.setText(placeholder);
    field.setForeground(Color.GRAY);

    field.addFocusListener(new java.awt.event.FocusAdapter() {
        @Override
        public void focusGained(java.awt.event.FocusEvent evt) {
            if (field.getText().equals(placeholder)) {
                field.setText("");
                field.setForeground(Color.BLACK);
            }
        }

        @Override
        public void focusLost(java.awt.event.FocusEvent evt) {
            if (field.getText().trim().equals("")) {
                field.setText(placeholder);
                field.setForeground(Color.GRAY);
            }
        }
    });
}

