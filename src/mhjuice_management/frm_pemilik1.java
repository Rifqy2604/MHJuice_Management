    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
       try {
    if (addMode) {
        // ===== TAMBAH DATA =====
        String namaPetugas = text_petugas1.getText().trim();
        String password = text_password1.getText().trim();

        if (namaPetugas.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
            return;
        }

        // Cek duplikasi nama_petugas
        String cekSql = "SELECT COUNT(*) FROM pengguna WHERE nama_pengguna = ? AND tugas = 1";
        PreparedStatement cekStat = k.getCon().prepareStatement(cekSql);
        cekStat.setString(1, namaPetugas);
        ResultSet rsCek = cekStat.executeQuery();
        rsCek.next();
        int count = rsCek.getInt(1);

        if (count > 0) {
            JOptionPane.showMessageDialog(null, "Nama petugas sudah ada. Silakan gunakan nama lain.");
            return;
        }

        String sql = "INSERT INTO pengguna (nama_pengguna, kata_sandi, tugas) VALUES (?, ?, ?)";
        PreparedStatement stat = k.getCon().prepareStatement(sql);
        stat.setString(1, namaPetugas);
        stat.setString(2, password);
        stat.setInt(3, 1); // tugas = 1 (petugas)
        stat.executeUpdate();

        JOptionPane.showMessageDialog(null, "Data petugas berhasil ditambahkan!");

        bersihkanField();
        text_petugas1.setEnabled(false);
        text_password1.setEnabled(false);

    } else if (editMode) {
        // ===== EDIT DATA =====
        String namaBaru = text_petugas2.getText().trim();
        String passwordBaru = text_password2.getText().trim();

        if (namaBaru.isEmpty() || passwordBaru.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
            return;
        }

        String sql = "UPDATE pengguna SET nama_pengguna = ?, kata_sandi = ? WHERE id_pengguna = ?";
        PreparedStatement stat = k.getCon().prepareStatement(sql);
        stat.setString(1, namaBaru);
        stat.setString(2, passwordBaru);
        stat.setString(3, idPetugasEdit);
        stat.executeUpdate();

        JOptionPane.showMessageDialog(null, "Data petugas berhasil diupdate!");

        bersihkanField();
        text_petugas2.setEnabled(false);
        text_password2.setEnabled(false);
    }

    refreshTable();

    // Reset mode
    addMode = false;
    editMode = false;

} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "SQL Error: " + e.getMessage());
}

    }//GEN-LAST:event_btn_simpanActionPerformed

